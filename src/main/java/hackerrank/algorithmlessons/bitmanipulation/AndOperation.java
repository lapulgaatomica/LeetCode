package hackerrank.algorithmlessons.bitmanipulation;

public class AndOperation {
    public static void main( String[] args) {
        int x = 12;
        int y = 10;
        System.out.println("Bitwise AND of (" + x + " , " + y + ") is: " + (x & y)); // yields to 8
        System.out.println(8 & 4);
    }
}
